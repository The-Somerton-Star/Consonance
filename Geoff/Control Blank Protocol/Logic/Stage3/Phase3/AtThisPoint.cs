using Geoff.Control_Blank_Protocol.Functions.Glance_of_Direction;

namespace Geoff.Control_Blank_Protocol.Logic.Stage3.Phase3
{
    public class AtThisPoint
    {
        public readonly Theory Idea;

        public AtThisPoint(Theory theory)
        {
            Idea = theory;
        }

        public char Providence()
        {
            return Idea.Algorithm;
        }

        public void Miraculize()
        {
            Idea.Miraculix(Idea.Algorithm);
        }
    }
}